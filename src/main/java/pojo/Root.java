package pojo;

public class Root 
{
	private String name;
	private String company;
	
	public Root()
	{
		
	}
	
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name=name;
	}
	
	public  String getCompany()
	{
		return company;
	}
	
	public void SetCompany(String company)
	{
		this.company=company;
	}

}
